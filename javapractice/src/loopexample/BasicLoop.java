package loopexample;

public class BasicLoop {

	public static void main(String[] args) {
		int num = 1;
		num += 2; // 1 + 2 = 3
		num += 3; // 3 + 3 = 6
		num += 4; // 6 + 4 = 10
		num += 5; // 10 + 5 = 15
		num += 6; // 15 + 6 = 21
		num += 7; // 21 + 7 = 28
		num += 8; // 28 + 8 = 36
		num += 9; // 36 + 9 = 45
		num += 10; // 45 + 10 = 55
		
		System.out.println("1부터 10까지의 합은 " + num + "입니다.");
	}

}
